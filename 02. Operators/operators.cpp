#include <iostream>
#include <cmath>
#include <climits>
using namespace std;

int main()
{    
    // OPERATORS
    int a = 2 / 5;
    cout << a << endl;
    cout << 2.0 / 5 << endl;

    int a = 4;
    int b = 3;

    bool first = (a != b);
    cout << first << endl;

    bool sec = (a <= b);
    cout << sec << endl;

    bool th = (a >= b);
    cout << th << endl;

    bool fo = (a == b);
    cout << fo << endl;
}