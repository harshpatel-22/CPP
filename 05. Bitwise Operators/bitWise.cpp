#include <iostream>
#include <cmath>
#include <climits>
using namespace std;

int main()
{
    // BITWISE OPERATOR
    // AND = & , OR = | , XOR = ^ , NOT = ~
    int a = 4;
    int b = 6;

    cout << "a & b = " << (a & b) << endl;
    cout << "a | b = " << (a | b) << endl;
    cout << "~a = " << (~a) << endl;
    cout << "a ^ b = " << (a ^ b) << endl;
}